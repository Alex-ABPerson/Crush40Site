                    <h2 class="title">2 Nights 2 Remember</h2>
                    <p>2 Nights 2 Remember is both a studio EP and a live album. The first four tracks on the CD are brand-new studio songs created for the EP, while the rest of the CD is professional audio of their <i>2 Nights 2 Remember</i> live performance, essentially "padding" the free space left on the CD.</p>
                    <p>The 2 Nights 2 Remember live performance was at Live Stage Guilty, on March 29th 2014 and March 30th 2014. Once again, both shows were professionally recorded and put on the CD in exactly the same manner as Live!.</p>
                    <p>The title of the album, <i>2 Nights 2 Remember</i> is a reference to the two shows Crush 40 performed in 2011 at Live Stage Guilty, and how much they meant to the band.</p>
