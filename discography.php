                    <p class="yearIndicator">2009</p>
                    <h2 class="title">Super Sonic Songs</h2>
                    <p>This is the first compilation album released by Crush 40. Comprised of a range of tracks from the band's ~10-year history. Some old tracks were remixed and the following new tracks were featured too: A cover of the vocal theme for "Sonic Riders: Zero Gravity", Un-gravitify, a cover of The Cult's Fire Woman, and Is It You - a fully original song created for this release, which was first original to be created by Crush 40.</p>
                    <p>While the original CD release of Super Sonic Songs did not feature any bonus tracks, a few years after its release, the album was placed on digital platforms with 3 bonus tracks: Free, With Me -Massive Power Mix-, and Seven Rings In Hand - these songs had been previously featured elsewhere, however.</p>
