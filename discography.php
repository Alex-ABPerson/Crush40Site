                    <h2 class="title">Crush 40</h2>
                    <p>This album marks the first time the name "Crush 40" was used. All of the vocal tracks from Thrill of the Feel are featured on this album, so in theory, this album is just a western release of that album. However, the instrumental tracks are missing and in their place are Live & Learn and two bonus tracks: It Doesn't Matter (with Tony Harnell on vocals), and Escape From The City (with Ted Poley & Tony Harnell on vocals).</p>
                    <p>The name "Crush 40" would be carried over to the game "Sonic Heroes", which would be released later in 2003.</p>
