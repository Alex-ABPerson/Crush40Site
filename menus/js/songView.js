function PopulateVersions(song)
{
    for (let group of song.versionGroups)   
    {
        let grpElem = document.createElement("li");
        grpElem.classList.add("group");

        // Title
        if (group.title)
        {
            let grpTitle = document.createElement("h3");
            grpTitle.innerText = group.title;
            grpElem.appendChild(grpTitle);
        }
        else grpElem.classList.add("noTitle");

        // Versions
        let versElem = document.createElement("ul");
        versElem.classList.add("versionList");
        versElem.classList.add("generalList");
        for (let version of group.versions)
        {
            let verElem = document.createElement("li");
            verElem.classList.add("versionGroupVerItem");
            verElem.addEventListener('click', () => SelectVersion(verElem, version, group));

            let verTitleElem = document.createElement("p");
            verTitleElem.classList.add("title");
            verTitleElem.innerText = version.fanName ? version.fanName : version.officialName;
            verElem.appendChild(verTitleElem);

            if (version.short)
            {
                let verDescElem = document.createElement("p");
                verDescElem.innerHTML = version.short;
                verElem.appendChild(verDescElem);
            }

            versElem.appendChild(verElem);
        }
        grpElem.appendChild(versElem);

        // Credits
        let creditsElem = document.createElement("ul");
        creditsElem.classList.add("credits");
        CreateCreditsItems(creditsElem, group.credits)

        grpElem.appendChild(creditsElem);

        // Add it to the correct list
        if (group.versions.length > 3)
            longGroupsList.appendChild(grpElem);
        else
            shortGroupsList.appendChild(grpElem);
    }
}

function SelectVersion(elem, version, group)
{
    // If it's already selected, just deselect it.
    let shouldSelectNew = !elem.classList.contains("selected");

    HideVersionInfoAndDeselect();

    if (shouldSelectNew)
    {
        elem.classList.add("selected");
        PopulateVersionInfo(version, group);
    }
}

function PopulateVersionInfo(version, group)
{
    ShowVersionInfo();

    // Title
    document.querySelector("#versionTitle").innerText = version.fanName ? version.fanName : version.officialName;
    document.querySelector("#versionTitleSub").innerText = version.fanName ? "(Unofficial Name)" : "(Official Name)";

    // Description
    document.querySelector("#versionDesc").innerHTML = version.long;

    // Is In Song
    let versionInSong = document.querySelector("#versionInSong");
    if (version.inGame)
    {
        versionInSong.classList.add("isInSong");
        versionInSong.innerText = "This version is featured in the game.";
    }
    else
    {
        versionInSong.classList.remove("isInSong");
        versionInSong.innerText = "This version is not featured in the game.";
    }

    // Appearances
    let appearancesLstElem = document.querySelector("#versionAppearances");
    let noAppearances = document.querySelector("#noAppearances");
    appearancesLstElem.replaceChildren();

    if (!version.appearances)
        noAppearances.classList.remove("hidden");
    else
    {
        noAppearances.classList.add("hidden");
        for (let appearance of version.appearances)
        {
            let appearanceItm = document.createElement("li");
            appearanceItm.innerHTML = "<p>" + appearance + "</p>";
            appearancesLstElem.appendChild(appearanceItm);
        }
    }

    // Credits
    CreateCreditsItems(document.querySelector("#versionCredits"), group.credits);
}

function ShowVersionInfo()
{
    document.querySelector("#versionInfo").classList.remove("hidden");
}

function HideVersionInfoAndDeselect()
{
    // Deselect the item 
    for (let desElem of document.querySelectorAll(".versionGroupVerItem"))
        desElem.classList.remove("selected");

    // Hide the version info
    document.querySelector("#versionInfo").classList.add("hidden");
}

function CreateCreditsItems(elem, credits)
{
    elem.replaceChildren();
    for (let credit of credits)
    {
        let creditElem = document.createElement("p");
        creditElem.innerHTML = 
            "<b>" + credit.t + "</b><br/>" + credit.n;
        
        elem.appendChild(creditElem);
    }
}
